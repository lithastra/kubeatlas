// PathPatterns returns just the URL patterns the route table contains,
// in the order Routes() returned them. Used by the contract test.
func (s *Server) PathPatterns() []string {
	out := make([]string, 0, len(s.Routes()))
	for _, r := range s.Routes() {
		out = append(out, r.Pattern)
	}
	sort.Strings(out)
	return out
}
