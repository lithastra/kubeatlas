// OpenAPI version + document metadata. Bump apiInfoVersion when the
// API surface changes in a backward-incompatible way; minor changes
// (a new endpoint, a new optional query param) bump the version's
// minor digit.
const (
	openAPIVersion = "3.0.3"
	apiInfoTitle   = "KubeAtlas API"
	apiInfoVersion = "v1alpha1"
	apiInfoLicense = "Apache-2.0"
	apiInfoLicURL  = "https://www.apache.org/licenses/LICENSE-2.0"
	apiInfoSummary = "Kubernetes resource dependency graph"
	apiInfoContact = "https://github.com/lithastra/kubeatlas"
)

// handleOpenAPI serves the spec at /api/v{version}/openapi.json.
// The version is inferred from the request path so the same
// handler powers both /api/v1alpha1/openapi.json and
// /api/v1/openapi.json — each renders only its version's paths
// and the version-appropriate component schemas.
func (s *Server) handleOpenAPI(w http.ResponseWriter, r *http.Request) {
	writeJSON(w, http.StatusOK, s.openAPISpecFor(apiVersionFor(r)))
}

// openAPISpecFor assembles the full document for one API version.
// The `paths` section is generated by walking Routes() and
// remapping patterns to the requested prefix; `components.schemas`
// is hand-written and version-aware (v1 carries the GA-only
// enrichment fields on ResourceDetailResponse; v1alpha1 keeps the
// frozen Phase 1 shape).
func (s *Server) openAPISpecFor(version string) map[string]any {
	return map[string]any{
		"openapi": openAPIVersion,
		"info": map[string]any{
			"title":   apiInfoTitle,
			"version": version,
			"summary": apiInfoSummary,
			"license": map[string]any{
				"name": apiInfoLicense,
				"url":  apiInfoLicURL,
			},
			"contact": map[string]any{
				"url": apiInfoContact,
			},
		},
		"paths":      s.openAPIPathsFor(version),
		"components": openAPIComponentsFor(version),
	}
}

// OpenAPISpecV1Alpha1 exposes the frozen v1alpha1 spec to
// out-of-process tooling — specifically the
// tools/api-compat-check dumper, which CI uses to detect any
// drift from the v0.1.0 baseline. Public so the tool can call
// it without copying the route-walking logic.
func (s *Server) OpenAPISpecV1Alpha1() map[string]any {
	return s.openAPISpecFor(APIVersionV1Alpha1)
}

// OpenAPISpecV1 is the GA-version twin. Same contract as
// OpenAPISpecV1Alpha1.
func (s *Server) OpenAPISpecV1() map[string]any {
	return s.openAPISpecFor(APIVersionV1)
}

// openAPIPathsFor builds the paths object for a specific API
// version. Patterns under /api/v1alpha1/... are remapped to the
// requested prefix; unversioned routes (/healthz, /readyz,
// /metrics) appear in both specs unchanged because operators
// hit those identically regardless of API version.
func (s *Server) openAPIPathsFor(version string) map[string]any {
	paths := map[string]any{}
	for _, r := range s.Routes() {
		// v1-only routes (e.g. /api/v1/federation/...) must not leak
		// into the v1alpha1 spec — that surface is frozen.
		if version == APIVersionV1Alpha1 && strings.HasPrefix(r.Pattern, apiPrefixV1) {
			continue
		}
		pattern := versionedPattern(r.Pattern, version)
		entry, ok := paths[pattern].(map[string]any)
		if !ok {
			entry = map[string]any{}
			paths[pattern] = entry
		}
		entry[strings.ToLower(r.Method)] = openAPIOperationFor(r, version)
	}
	return paths
}

// openAPIOperationFor adjusts the version-sensitive parts of an
// operation entry (currently just the response schema reference
// for ResourceDetailResponse, which carries the v1-only enrichment
// fields).
func openAPIOperationFor(r RouteInfo, version string) map[string]any {
	op := openAPIOperation(r)
	if version == APIVersionV1 && r.Response.SchemaRef == "ResourceDetailResponse" {
		// Override the success-content $ref to point at the v1
		// shape so the spec advertises the GA-only fields.
		if responses, ok := op["responses"].(map[string]any); ok {
			if okResp, ok := responses["200"].(map[string]any); ok {
				if content, ok := okResp["content"].(map[string]any); ok {
					if app, ok := content["application/json"].(map[string]any); ok {
						app["schema"] = map[string]any{
							"$ref": "#/components/schemas/ResourceDetailResponseV1",
						}
					}
				}
			}
		}
	}
	return op
}

// openAPIOperation renders one Operation object for a route.
func openAPIOperation(r RouteInfo) map[string]any {
	op := map[string]any{
		"summary":     r.Summary,
		"description": r.Description,
		"operationId": operationID(r),
		"responses":   openAPIResponses(r),
	}
	params := openAPIParams(r)
	if len(params) > 0 {
		op["parameters"] = params
	}
	return op
}

// openAPIParams converts the route's path + query params into the
// OpenAPI parameters list, deterministic-ordered.
func openAPIParams(r RouteInfo) []map[string]any {
	out := make([]map[string]any, 0, len(r.PathParams)+len(r.QueryParams))
	for _, p := range r.PathParams {
		out = append(out, openAPIParam(p, "path"))
	}
	for _, p := range r.QueryParams {
		out = append(out, openAPIParam(p, "query"))
	}
	return out
}

func openAPIParam(p ParamSpec, in string) map[string]any {
	schema := map[string]any{"type": p.Type}
	if len(p.Enum) > 0 {
		// Convert []string to []any so json.Marshal emits the JSON
		// array verbatim.
		enum := make([]any, 0, len(p.Enum))
		for _, v := range p.Enum {
			enum = append(enum, v)
		}
		schema["enum"] = enum
	}
	out := map[string]any{
		"name":     p.Name,
		"in":       in,
		"required": p.Required || in == "path", // path params are always required
		"schema":   schema,
	}
	if p.Description != "" {
		out["description"] = p.Description
	}
	return out
}

// openAPIResponses renders the responses map for a route. Every route
// declares its 200 success; common error codes are referenced via
// components/responses.
func openAPIResponses(r RouteInfo) map[string]any {
	contentType := r.Response.ContentType
	if contentType == "" {
		contentType = "application/json"
	}
	successCode := "200"
	if r.Pattern == "/api/v1alpha1/watch" {
		successCode = "101" // WebSocket upgrade
	}
	successResp := map[string]any{
		"description": r.Response.Description,
	}
	if r.Response.SchemaRef != "" {
		successResp["content"] = map[string]any{
			contentType: map[string]any{
				"schema": map[string]any{
					"$ref": "#/components/schemas/" + r.Response.SchemaRef,
				},
			},
		}
	}
	resps := map[string]any{
		successCode: successResp,
	}
	// /readyz can return 503 when the informer is still syncing.
	if r.Pattern == "/readyz" {
		resps["503"] = map[string]any{
			"description": "Service not ready (informer cache still syncing)",
			"content": map[string]any{
				"application/json": map[string]any{
					"schema": map[string]any{"$ref": "#/components/schemas/HealthResponse"},
				},
			},
		}
	}
	// All v1alpha1 endpoints can return the standard error envelopes.
	if strings.HasPrefix(r.Pattern, "/api/v1alpha1/") && r.Pattern != "/api/v1alpha1/watch" {
		resps["400"] = errorRef("Invalid request (missing or malformed parameters)")
		resps["404"] = errorRef("Resource not found")
		resps["500"] = errorRef("Internal server error")
	}
	return resps
}

func errorRef(desc string) map[string]any {
	return map[string]any{
		"description": desc,
		"content": map[string]any{
			"application/json": map[string]any{
				"schema": map[string]any{"$ref": "#/components/schemas/ErrorResponse"},
			},
		},
	}
}

// operationID builds a stable identifier for a route, derived from
// method + pattern. e.g. "get_api_v1alpha1_graph",
// "get_api_v1alpha1_resources_namespace_kind_name_incoming".
func operationID(r RouteInfo) string {
	clean := strings.NewReplacer("/", "_", "{", "", "}", "", ".", "_").Replace(r.Pattern)
	clean = strings.Trim(clean, "_")
	return strings.ToLower(r.Method) + "_" + clean
}

